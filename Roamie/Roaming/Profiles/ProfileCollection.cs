using System;
using System.Collections.Generic;
using System.Text;
using System.Runtime.Serialization;

namespace Virtuoso.Miranda.Roamie.Roaming.Profiles
{
    [Serializable]
    public sealed class ProfileCollection : List<RoamingProfile>
    {
        internal ProfileCollection() : base(1) { }

        public RoamingProfile Find(string name)
        {
            return Find(delegate(RoamingProfile _profile)
            {
                return _profile.Name == name;
            });
        }
    }
}
